    private void ShowDataTable() {
        ResultSet res = null;
        try {
            res = ban.LoadAllBanData();
            ClearData();
            while (res.next()) {
                String rows[] = new String[2];
                rows[0] = res.getString(1);
                rows[1] = res.getString(2);
                model.addRow(rows);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void ClearData() {
        int n = model.getRowCount() - 1;
        for (int i = n; i >= 0; i--) {
            model.removeRow(i);
        }
    }
